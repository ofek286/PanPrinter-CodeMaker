    private void codeExport(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_codeExport
        //resetArray(_sPos);
        _sPos = new Coordinates[5];
        int maxLevel;
        if (l5S_exists)
            maxLevel = 4;
        else if (l4S_exists)
            maxLevel = 3;
        else if (l3S_exists)
            maxLevel = 2;
        else if (l2S_exists)
            maxLevel = 1;
        else if (l1S_exists)
            maxLevel = 0;
        else
        {
            _code = "No starting posion selected";
            codeUpdate();
            return;
        }
        
        HeatLevel[][] grid = new HeatLevel[16][16];
        //resetArray(grid);
        for (int i = 0; i < 16; i++)
        {
            for (int j = 0; j < 16; j++)
            {
                String temp = "l" + i + "x" + j;
                JLabel curr = (JLabel) getResource(temp);
                HeatLevel currLevel = getJLabelHeatLevel(curr);
                switch (currLevel)
                {
                    case L1S: _sPos[0] = new Coordinates(i, j); break;
                    case L2S: _sPos[1] = new Coordinates(i, j); break;
                    case L3S: _sPos[2] = new Coordinates(i, j); break;
                    case L4S: _sPos[3] = new Coordinates(i, j); break;
                    case L5S: _sPos[4] = new Coordinates(i, j); break;
                }
                grid[i][j] = currLevel;
            }
        }
        for (int i = 0; i < 16; i++)
        {
            for (int j = 0; j < 16; j++)
            {
                System.out.print(grid[i][j] + "\t");
            }
            System.out.println("");
        }
        
        for (Coordinates _sPo : _sPos) {
            System.out.println(_sPo);
        }
        //System.out.println(getJLabelHeatLevel((JLabel) getResource("l8x8")));
        
        _code = "helloPanSys\n"
              + "checkCon " + ((int) (Math.random()*10000)) + "\n"
              + "defMethod OSGrid\n"
              + "gridDef 16C\n"
              + "levelsDef " + (maxLevel+1) + "\n";
        
        for (int i = 0; i <= maxLevel; i++)
        {
            _code += "startPos " + _sPos[i].getX() + "," + _sPos[i].getY() + "\n"
                   + "doLevel " + (i + 1) + "\n";
            findPath(_sPos[i].getX(), _sPos[i].getY(), grid);
            _code += "endLevel\n";
        }
        _code += "endPrint";
        codeUpdate();
    }//GEN-LAST:event_codeExport

