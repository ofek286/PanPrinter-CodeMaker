    private void findPath(int x, int y, HeatLevel[][] grid) {
        HeatLevel currLevel = grid[x][y];
        Coordinates thisC = new Coordinates(x, y);
        if ((!thisC.equals(_sPos[0])) && (!thisC.equals(_sPos[1])) && (!thisC.equals(_sPos[2])) && (!thisC.equals(_sPos[3])) && (!thisC.equals(_sPos[4])))
            _code += "movTo " + x + "," + y + "\n";
        switch (currLevel)
        {
            case L1S: currLevel = HeatLevel.L1; break;
            case L2S: currLevel = HeatLevel.L2; break;
            case L3S: currLevel = HeatLevel.L3; break;
            case L4S: currLevel = HeatLevel.L4; break;
            case L5S: currLevel = HeatLevel.L5; break;
        }
        if ((x-1 >= 0) && (grid[x-1][y] == currLevel))
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x-1, y, grid);
        }
        else if ((x-1 >= 0) && (y-1 >=0) && (grid[x-1][y-1] == currLevel))
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x-1, y-1, grid);
        }
        else if ((y-1 >=0) && (grid[x][y-1] == currLevel))
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x, y-1, grid);
        }
        else if ((x+1 <= 15) && (y-1 >= 0) && (grid[x+1][y-1] == currLevel))
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x+1, y-1, grid);
        }
        else if ((x+1 <= 15) && (grid[x+1][y] == currLevel))
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x+1, y, grid);
        }
        else if ((x+1 <= 15) && (y+1 <= 15) && (grid[x+1][y+1] == currLevel))
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x+1, y+1, grid);
        }
        else if ((y+1 <= 15) && (grid[x][y+1] == currLevel))
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x, y+1, grid);
        }
        else if ((x-1 >= 0) && (y+1 <= 15) && grid[x-1][y+1] == currLevel)
        {
            grid[x][y] = HeatLevel.L0;
            findPath(x-1, y+1, grid);
        }
        else
        {
            grid[x][y] = HeatLevel.L0;
        }
    }
    
